package exam.oop.instance.ex;

public class Calc01 {
	
	public static int plus(int a, int b){
		return a+b;
	}
	public static int minus(int a, int b){
		return a-b;
	}
	public static int multiple(int a, int b){
		return a*b;
	}
	public static int divide(int a, int b){
		return a/b;
	}
	
}
